public class Operators {
    public static void main(String[] args){
        int a = 10;
        int b = 35;
        //Arithmatic operators
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println(++a);
        System.out.println(--b);
    }
}
